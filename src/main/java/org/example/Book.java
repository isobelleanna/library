package org.example;

public class Book {
    String title;
    String author;
    String genre;
    String subGenre;
    String publisher;

    public Book(String title, String author, String genre, String subGenre, String publisher) {
        this.title = title;
        this.author = author;
        this.genre = genre;
        this.subGenre = subGenre;
        this.publisher = publisher;
    }
}
